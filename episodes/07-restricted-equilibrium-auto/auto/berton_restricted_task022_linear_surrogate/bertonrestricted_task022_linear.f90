
SUBROUTINE FUNC(NDIM,U,ICP,PAR,IJAC,F,DFDU,DFDP)
  IMPLICIT NONE
  INTEGER, INTENT(IN) :: NDIM,IJAC,ICP(*)
  DOUBLE PRECISION, INTENT(IN) :: U(NDIM),PAR(*)
  DOUBLE PRECISION, INTENT(OUT) :: F(NDIM)
  DOUBLE PRECISION, INTENT(INOUT) :: DFDU(NDIM,NDIM),DFDP(NDIM,*)
  DOUBLE PRECISION :: A(3,3), B(3), DW
  INTEGER :: I,J
  DATA A / -4.99993750295296413E-01, -3.96390273157912632E-03, 7.03184308838957151E+00, -9.99987500114679073E-01, 0.00000000000000000E+00, 0.00000000000000000E+00, -2.74531320540558559E-16, -9.99999999212004442E-01, -9.97524552150732768E-01 /
  DATA B / 1.07622260832937620E-16, 3.08803268707093315E+00, -1.10801051170172831E-10 /
  DW=PAR(2)-0.6D0
  DO I=1,3
    F(I)=B(I)*DW
    DO J=1,3
      F(I)=F(I)+A(I,J)*U(J)
    END DO
  END DO
  IF (IJAC .NE. 0) THEN
    DFDU(1:NDIM,1:NDIM)=0D0
    DFDP(1:NDIM,1:98)=0D0
    DO I=1,3
      DO J=1,3
        DFDU(I,J)=A(I,J)
      END DO
      DFDP(I,2)=B(I)
    END DO
  END IF
END SUBROUTINE FUNC

